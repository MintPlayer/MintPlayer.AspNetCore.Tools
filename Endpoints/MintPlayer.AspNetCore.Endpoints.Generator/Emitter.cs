        sb.AppendLine("    private static async global::System.Threading.Tasks.ValueTask DisposeEndpointAsync(object endpoint)");
        sb.AppendLine("    {");
        sb.AppendLine("        if (endpoint is global::System.IAsyncDisposable asyncDisposable)");
        sb.AppendLine("            await asyncDisposable.DisposeAsync();");
        sb.AppendLine("        else if (endpoint is global::System.IDisposable disposable)");
        sb.AppendLine("            disposable.Dispose();");
        sb.AppendLine("    }");
        sb.AppendLine();

        // CreateDescriptor<TEndpoint>
        sb.AppendLine("    private static global::MintPlayer.AspNetCore.Endpoints.EndpointDescriptor CreateDescriptor<TEndpoint>(string name)");
        sb.AppendLine("        where TEndpoint : global::MintPlayer.AspNetCore.Endpoints.IEndpointBase");
        sb.AppendLine("    {");
        sb.AppendLine("        return new(name, TEndpoint.Path, TEndpoint.Methods, typeof(TEndpoint));");
        sb.AppendLine("    }");
    }

    private static void EmitMetadataProperty(StringBuilder sb, List<EndpointInfo> endpoints)
    {
        sb.AppendLine("    public static global::System.Collections.Generic.IReadOnlyList<global::MintPlayer.AspNetCore.Endpoints.EndpointDescriptor> Endpoints { get; } =");
        sb.AppendLine("    [");
        foreach (var endpoint in endpoints)
        {
            sb.AppendLine($"        CreateDescriptor<{endpoint.FullyQualifiedName}>(\"{endpoint.ClassName}\"),");
        }
        sb.AppendLine("    ];");
        sb.AppendLine();
    }

    private static string GetFactoryFieldName(EndpointInfo endpoint)
    {
        // Use a sanitized version of the class name to avoid collisions
        var safeName = endpoint.FullyQualifiedName
            .Replace("global::", "")
            .Replace(".", "_");
        return $"_factory_{safeName}";
    }
}
